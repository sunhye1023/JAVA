package quiz38;

public class Data {

	private String date;
	private String hskName;
	private String code;
	private String fishName;
	private String exim;
	private String count;
	private String money;
	private String result;
	private String price;
	private String day;
	
	public Data() {
		
	}	
	
	public Data(String date, String hskName, String code, String fishName, String exim, String count, String money,
			String result, String price, String day) {
		super();
		this.date = date;
		this.hskName = hskName;
		this.code = code;
		this.fishName = fishName;
		this.exim = exim;
		this.count = count;
		this.money = money;
		this.result = result;
		this.price = price;
		this.day = day;
	}

	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getHskName() {
		return hskName;
	}
	public void setHskName(String hskName) {
		this.hskName = hskName;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getFishName() {
		return fishName;
	}
	public void setFishName(String fishName) {
		this.fishName = fishName;
	}
	public String getExim() {
		return exim;
	}
	public void setExim(String exim) {
		this.exim = exim;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	
	
	
}
